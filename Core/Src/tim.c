/**
  * @brief  Period elapsed callback in non-blocking mode
  * @param  htim TIM handle
  * @retval None
  */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
  static uint16_t MK_OK_LedState = MK_OK_LED_OFF;
  GPIO_PinState PinState;
  
  //Пока VPC3 не в состоянии обмена, мигаем индикатором MK_OK
  if(DP_State != DATA_EX){
    MK_OK_LedState = !MK_OK_LedState; //меняем состояние светодиода на противоположное
    PinState = (MK_OK_LedState == MK_OK_LED_OFF) ? GPIO_PIN_RESET : GPIO_PIN_SET;
    HAL_GPIO_WritePin(GPIOC, GPIO_PIN_14, PinState);
  }else{ //Состояние обмена. Включаем постоянное свечение индикатора MK_OK
    MK_OK_LedState = MK_OK_LED_ON;
    PinState = GPIO_PIN_SET;
    HAL_GPIO_WritePin(GPIOC, GPIO_PIN_14, PinState);
  }
  
}

